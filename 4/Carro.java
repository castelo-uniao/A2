class Carro 
{
    String cor;
    String modelo;
    int ano;
    double valor;
    String marca;

    public Carro() 
    {

    }

    public Carro(String cor, String modelo, int ano, double valor, String marca) 
    {
        this.cor = cor;
        this.modelo = modelo;
        this.ano = ano;
        this.valor = valor;
        this.marca = marca;
    }
}